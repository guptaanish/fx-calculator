package com.fxcalculator.config;

public class FxCalculatorMatrixException extends Exception {

	private static final long serialVersionUID = 1L;

	public FxCalculatorMatrixException() {
		super();
	}

	public FxCalculatorMatrixException(final String message, final Throwable cause, final boolean enableSuppression, final boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FxCalculatorMatrixException(final String message, final Throwable cause) {
		super(message, cause);
	}

	public FxCalculatorMatrixException(final String message) {
		super(message);
	}

	public FxCalculatorMatrixException(final Throwable cause) {
		super(cause);
	}

}
